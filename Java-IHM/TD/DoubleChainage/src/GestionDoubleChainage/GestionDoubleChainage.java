    private void clearAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearAction
        // Ici on nettoie l'affichage et la liste chaînée.
        listeString.clear();
        if((listeString.toString()).equals("[]")){
            afficheListe.setText("");
            saisieTexte.requestFocus();
            javax.swing.JOptionPane.showMessageDialog(this,"La liste est vidée.","Information",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_clearAction

