    private void ajoutFinAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajoutFinAction
        // On ajoute l'élément saisie à la fin de listeString.
        // Si rien n'est saisie on informe l'utilisateur et on insert rien.
        if(saisieTexte.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Rien à insérer","Information",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }else{
            listeString.addLast(saisieTexte.getText());
            afficheListe.setText(listeString.toString());
            saisieTexte.setText("");
            saisieTexte.requestFocus();
        }
    }//GEN-LAST:event_ajoutFinAction

    private void ajoutDebutAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajoutDebutAction
        // On ajoute l'élément saisie au début de listeString.
        // Si rien n'est saisie on informe l'utilisateur et on insert rien.
        if(saisieTexte.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(this,"Rien à insérer","Information",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }else{
            listeString.addFirst(saisieTexte.getText());
            afficheListe.setText(listeString.toString());
            saisieTexte.setText("");
            saisieTexte.requestFocus();
        }
    }//GEN-LAST:event_ajoutDebutAction

    private void retireDebutAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retireDebutAction
        // On retire l'élément au début de listeString.
        // Si la listeString est vide on informe de l'erreur à l'utilisateur;
        try{
            listeString.removeFirst();
            if((listeString.toString()).equals("[]")){
                afficheListe.setText("");
                saisieTexte.requestFocus();
            } else{
                afficheListe.setText(listeString.toString());
                saisieTexte.requestFocus();
            }
        }catch( java.util.NoSuchElementException e){
            javax.swing.JOptionPane.showMessageDialog(this,"Rien à retirer","Erreur",javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_retireDebutAction

    private void retireFinAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retireFinAction
        // On retire l'élément à la fin de listeString.
        // Si la listeString est vide on informe de l'erreur à l'utilisateur;
        try{
            listeString.removeLast();
            if((listeString.toString()).equals("[]")){
                afficheListe.setText("");
                saisieTexte.requestFocus();
            } else{
                afficheListe.setText(listeString.toString());
                saisieTexte.requestFocus();
            }
        }catch(java.util.NoSuchElementException e){
             javax.swing.JOptionPane.showMessageDialog(this,"Rien à retirer","Erreur",javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_retireFinAction

